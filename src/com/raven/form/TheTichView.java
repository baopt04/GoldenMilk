    public TheTich readForm() {
        String ma, ten;
        ma = txtmatt.getText();
        ten = txttentt.getText();
        return new TheTich(ma, ten);
    }

    public void fillTable(List<TheTich> list) {
        tableModel = (DefaultTableModel) tbltable.getModel();
        tableModel.setRowCount(0);
        for (TheTich theTich : list) {
            tableModel.addRow(theTich.toDataRowTT());
        }
    }

    public boolean CheckData() {
        if (txtmatt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã");
            return false;
        }
        if (txttentt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên");
            return false;
        }
        return true;
    }

    public void showData(int index) {
        TheTich doiTuong = theTichSerivce.getList().get(index);
        txtmatt.setText(doiTuong.getMaTheTich());
        txttentt.setText(doiTuong.getTenTheTich());
    }

