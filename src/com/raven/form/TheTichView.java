    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (CheckData()) {
            TheTich doituong = this.readForm();
            if (theTichSerivce.add(doituong) > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                txtmatt.setText("");
                txttentt.setText("");
                fillTable(theTichSerivce.getList());
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }

        }    // TODO add your handling code here:
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        index = tbltable.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn trường cần xóa");
        } else {
            String ma = tbltable.getValueAt(index, 0).toString();
            if (theTichSerivce.delete(ma) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                fillTable(theTichSerivce.getList());
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }     // TODO add your handling code here:
    }//GEN-LAST:event_jButton4MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        index = tbltable.getSelectedRow();
        TheTich doiTuong = this.readForm();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn trường cần sửa");
        } else {
            String id = tbltable.getValueAt(index, 0).toString();
            if (theTichSerivce.update(doiTuong, id) > 0) {
                JOptionPane.showMessageDialog(this, "Update thành công");
                fillTable(theTichSerivce.getList());
                txtmatt.setText("");
                txttentt.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Update thất bại ");
            }
        }  // TODO add your handling code here:
    }//GEN-LAST:event_jButton3MouseClicked

