    private void btnxoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnxoaMouseClicked
        index = tbldoituong.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn trường cần xóa");
        } else {
            String ma = tbldoituong.getValueAt(index, 0).toString();
            if (doiTuongService.delete(ma) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                fillTable(doiTuongService.getList());
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }
    }//GEN-LAST:event_btnxoaMouseClicked

    private void tbldoituongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldoituongMouseClicked
        index = tbldoituong.getSelectedRow();
        this.showData(index);// TODO add your handling code here:
    }//GEN-LAST:event_tbldoituongMouseClicked

    private void btnupdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnupdateMouseClicked
        index = tbldoituong.getSelectedRow();
        DoiTuong doiTuong = this.readForm();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn trường cần sửa");
        } else {
            String id = tbldoituong.getValueAt(index, 0).toString();
            if (doiTuongService.update(doiTuong, id) > 0) {
                JOptionPane.showMessageDialog(this, "Update thành công");
                fillTable(doiTuongService.getList());
                txtmadt.setText("");
                txttendt.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Update thất bại ");
            }
        }
// TODO add your handling code here:
    }//GEN-LAST:event_btnupdateMouseClicked

