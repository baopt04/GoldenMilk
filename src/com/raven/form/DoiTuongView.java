public DoiTuong readForm() {
        String ma, ten;
        ma = txtmadt.getText();
        ten = txttendt.getText();
        return new DoiTuong(ma, ten);
    }

    public void fillTable(List<DoiTuong> list) {
        tableModel = (DefaultTableModel) tbldoituong.getModel();
        tableModel.setRowCount(0);
        for (DoiTuong doiTuong : list) {
            tableModel.addRow(doiTuong.toDataRowDT());
        }
    }

    public boolean CheckData() {
        if (txtmadt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã");
            return false;
        }
        if (txttendt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên");
            return false;
        }
        return true;
    }

    public void showData(int index) {
        DoiTuong doiTuong = doiTuongService.getList().get(index);
        txtmadt.setText(doiTuong.getMaDoiTuong());
        txttendt.setText(doiTuong.getTenDoiTuongSD());
    }

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (CheckData()) {
            DoiTuong doituong = this.readForm();
            if (doiTuongService.addDoiTuong(doituong) > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                txtmadt.setText("");
                txttendt.setText("");
                fillTable(doiTuongService.getList());
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1MouseClicked

