    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
EmployeeDAO employeeDAO = new EmployeeDAO();
        System.out.println("manv: "+ employeeDAO.getMaNV());
    if (!employeeService.validatePhone(jTextField2.getText())) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ");
        } else if (!employeeService.validateEmail(jTextField3.getText())) {
            JOptionPane.showMessageDialog(this, "Email không hợp lệ");
        } else if (employeeService.isEmailOrPhoneExists(jTextField3.getText(), jTextField2.getText(), employeeDAO.getMaNV())) {
            JOptionPane.showMessageDialog(this, "Số điện thoại hoặc email đã bị trùng");
        } else {
            updateEmployeeInfo(employeeDAO.getMaNV());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

