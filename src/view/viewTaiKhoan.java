    private void updateEmployeeInfo(String maNV) {
        String name = jTextField1.getText();
        String phone = jTextField2.getText();
        String email = jTextField3.getText();
        String gender = jRadioButton1.isSelected() ? "0" : "1";
        java.util.Date birthDate = jDateChooser1.getDate();
        String address = jTextField4.getText();

        Employee updatedEmployee = new Employee();
        updatedEmployee.setFullName(name);
        updatedEmployee.setPhoneNumber(phone);
        updatedEmployee.setEmail(email);
        updatedEmployee.setGender(gender);
        updatedEmployee.setBirthDate(birthDate);
        updatedEmployee.setAddress(address);
        updatedEmployee.setMaNV(maNV);
        boolean isUpdated = employeeService.updateEmployee(updatedEmployee);

        if (isUpdated) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void hienThiThongTinNhanVien(EmployeeDAO employee) {
        if (employee != null) {
            jTextField1.setText(employee.getFullName());
            jTextField2.setText(employee.getPhoneNumber());
            jTextField3.setText(employee.getEmail());
            if ("0".equals(employee.getGender())) {
                jRadioButton1.setSelected(true);
            } else if ("1".equals(employee.getGender())) {
                jRadioButton2.setSelected(true);
            }
            jDateChooser1.setDate(employee.getBirthDate());
            jTextField4.setText(employee.getAddress());
        }
    }

